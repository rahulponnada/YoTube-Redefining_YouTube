package edu.umkc.ranking;

public class VideoInfo {

	String videoId;
	String title;
	String thumbnail;
	String viewCount;
	String likeCount;
	String dislikeCount;
	String commentCount;
	int ranking;	
	String likeviewCount;
	String dislikelikeCount;
	String viewpercent;
	String url;
		
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getLikeviewCount() {
		return likeviewCount;
	}
	public void setLikeviewCount(String likeviewCount) {
		this.likeviewCount = likeviewCount;
	}
	public String getDislikelikeCount() {
		return dislikelikeCount;
	}
	public void setDislikelikeCount(String dislikelikeCount) {
		this.dislikelikeCount = dislikelikeCount;
	}
	public String getViewpercent() {
		return viewpercent;
	}
	public void setViewpercent(String viewpercent) {
		this.viewpercent = viewpercent;
	}
	public int getRanking() {
		return ranking;
	}
	public void setRanking(int ranking) {
		this.ranking = ranking;
	}
	public String getVideoId() {
		return videoId;
	}
	public void setVideoId(String videoId) {
		this.videoId = videoId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	public String getViewCount() {
		return viewCount;
	}
	public void setViewCount(String viewCount) {
		this.viewCount = viewCount;
	}
	public String getLikeCount() {
		return likeCount;
	}
	public void setLikeCount(String likeCount) {
		this.likeCount = likeCount;
	}
	public String getDislikeCount() {
		return dislikeCount;
	}
	public void setDislikeCount(String dislikeCount) {
		this.dislikeCount = dislikeCount;
	}
	public String getCommentCount() {
		return commentCount;
	}
	public void setCommentCount(String commentCount) {
		this.commentCount = commentCount;
	}
	
}
